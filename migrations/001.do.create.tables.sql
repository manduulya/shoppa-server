CREATE TABLE shoppa_users (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL
);
CREATE TABLE shoppa_shoppinglists (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    title TEXT NOT NULL,
    user_id INTEGER REFERENCES shoppa_users(id) ON DELETE CASCADE,
    date_created TIMESTAMPTZ DEFAULT now() NOT NULL

);
CREATE TABLE shoppa_stores (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    shopping_list_id INTEGER REFERENCES shoppa_shoppinglists(id) ON DELETE CASCADE NOT NULL
);

CREATE TABLE shoppa_items (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL,
    store_id INTEGER REFERENCES shoppa_stores(id) ON DELETE CASCADE NOT NULL
);


